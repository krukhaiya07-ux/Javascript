//Topic:Types of Arithmetic Operators

console.log("Operators in js")
let a = 10;
let b = 4;
console.log("a+b=",a+b)
console.log("a-b=",a-b)
console.log("a/b=",a/b)
console.log("a**b=",a**b)
//INCREMENT USING OPERATORS
console.log("++a=",++a)//Increments first, Store later;value=11
console.log("a++=",a++)//increments in later stage, only store the data;value=12
console.log("--a=",--a)//value=11
console.log("a--=",a--)//value=10
console.log("a=",a)// prints the current value 10
console.log("a--=",a--)//value=9
console.log("a=",a)